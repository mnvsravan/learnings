#include <stdio.h>

int main(){
    int radius; // Declare variable for radius
    radius=6; // Initialize radius with a value
    int height; // Declare variable for height
    height=10; // Initialize height with a value

    printf("The area of cylinder is: %.2f\n", 3.14 * radius * radius * height); // Calculate and print area of cylinder
    return 0;
}
